package lesson3;

public class Animals {
   private int weight;
    private int height;

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public Animals(int weight, int height) {
        this.weight = weight;
        this.height = height;
    }


}
